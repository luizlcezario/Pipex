#include "libft_bonus.h"

void	*ft_memset(void *ptr, int x, size_t n)
{
	size_t	a;

	a = 0;
	while (a < n)
	{
		*(unsigned char *)(ptr + a) = x;
		a++;
	}
	return (ptr);
}
